int	ft_str_is_alpha(char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		if ((src[i] < 'A' || src[i] > 'Z') && (src[i] < 'a' || src[i] > 'z'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
