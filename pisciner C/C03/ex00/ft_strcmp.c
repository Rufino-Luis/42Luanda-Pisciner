int	ft_strcmp(char *s1, char *s2)
{
	int	cont;

	cont = 0;
	while (s1[cont] == s2[cont] && s1[cont] != '\0')
	{
		cont++;
	}
	if (s1[cont] > s2[cont])
	{
		return (1);
	}
	else if (s1[cont] < s2[cont])
	{
		return (-1);
	}
	return (0);
}
