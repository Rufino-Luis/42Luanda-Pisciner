int	ft_is_prime(int nb)
{
	int	num;
	int	cont;

	num = nb;
	cont = 0;
	while (nb > 0)
	{
		if (num % nb == 0)
		{
			cont++;
		}
		nb --;
	}
	if (cont == 2)
		return (1);
	else
		return (0);
}
